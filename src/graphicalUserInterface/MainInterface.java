    public MainInterface(module.DBConsole db, String childID){
        this();
        this.db=db;
        this.childID=childID;
        
        String [][]dataChild = db.doQuery("SELECT * FROM child where id = '"+childID+"'", 0);
        String [][]dataVisit = db.doQuery("SELECT * FROM visit where child_id = '"+childID+
                "' and visit_date = (SELECT MAX(visit_date) FROM visit WHERE child_id='"+childID+"')", 0);
        
        setGeneralInfo(dataChild[0]);
        
        zScore.person = new data.Person(
                dataChild[0][1],
                dataChild[0][2].contains("laki"),
                dataChild[0][4], 
                dataVisit[0][1],
                Double.parseDouble(dataVisit[0][2]),
                Double.parseDouble(dataVisit[0][3])
        );
        
        setLatestInfo(dataVisit[0]);
        
        double[]z = {
            zScore.countWFA(zScore.person.isMale(), zScore.person.getAgeByDay(), zScore.person.getWeight()),
            zScore.countHFA(zScore.person.isMale(), zScore.person.getAgeByDay(), zScore.person.getHeight()),
            zScore.person.getAgeByDay() < 730 ?
            zScore.countWFL(zScore.person.isMale(), zScore.person.getWeight(), zScore.person.getHeight()) :
            zScore.countWFH(zScore.person.isMale(), zScore.person.getWeight(), zScore.person.getHeight())
        };
        String []zStatus = {
            zScore.getWFAZScoreClassification(z[0]),
            zScore.getHFAZScoreClassification(z[1]),
            zScore.getWFLZScoreClassification(z[2])
        };
        
        setLatestResult(new String[]{
            Double.toString(new BigDecimal(z[0])
                            .setScale(3, BigDecimal.ROUND_HALF_UP)
                            .doubleValue()),
            zStatus[0],
            Double.toString(new BigDecimal(z[1])
                            .setScale(3, BigDecimal.ROUND_HALF_UP)
                            .doubleValue()),
            zStatus[1],
            Double.toString(new BigDecimal(z[2])
                            .setScale(3, BigDecimal.ROUND_HALF_UP)
                            .doubleValue()),
            zStatus[2]
        });
    }

